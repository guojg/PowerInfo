package com.github.balance.parparedata.powerquotient.entity;


public class QuotientData {

	private String index_item ;
	private String year ;
	private String task_id ;
	private Double value ;
	
	public String getIndex_item() {
		return index_item;
	}
	public void setIndex_item(String index_item) {
		this.index_item = index_item;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getTask_id() {
		return task_id;
	}
	public void setTask_id(String task_id) {
		this.task_id = task_id;
	}
	public Double getValue() {
		return value;
	}
	public void setValue(Double value) {
		this.value = value;
	}
	

	
}
