package com.github.regionalanalysis.fx.generatorcontrast.model;


public class GeneratorContrast {
	
	private String indexX;
	private String indexY;
	private String value;
	private String unit;

	public String getIndexX() {
		return indexX;
	}
	public void setIndexX(String indexX) {
		this.indexX = indexX;
	}
	public String getIndexY() {
		return indexY;
	}
	public void setIndexY(String indexY) {
		this.indexY = indexY;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	

}
